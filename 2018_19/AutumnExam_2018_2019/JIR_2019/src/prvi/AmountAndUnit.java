package prvi;

public class AmountAndUnit {

	private double amount;
	private Unit unit;
	
	public AmountAndUnit(double amount, Unit unit) {
		this.amount = amount;
		this.unit = unit;
	}
	
	public double getAmount() {
		return amount;
	}
	
	public Unit getUnit() {
		return unit;
	}
		
}

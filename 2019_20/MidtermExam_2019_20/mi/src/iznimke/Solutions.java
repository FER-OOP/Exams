package iznimke;

public class Solutions {

	private double smaller;
	private double bigger;
	
	public Solutions(double smaller, double bigger) {
		super();
		this.smaller = smaller;
		this.bigger = bigger;
	}
	
	public double getSmaller() {
		return smaller;
	}
	
	public double getBigger() {
		return bigger;
	}
}

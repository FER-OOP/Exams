package hr.fer.oop.midterm_2018_19.task3;

public class Person {	
	private String id;
	private String name;
	
	public Person(String id, String name) {
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}	
	
	public String getName() {
		return name;
	}
}
